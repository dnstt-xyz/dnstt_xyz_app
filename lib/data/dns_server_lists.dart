/// Predefined DNS server lists that can be imported into the app
class DnsServerList {
  final String id;
  final String country;
  final String countryCode;
  final String description;
  final List<DnsServerEntry> servers;

  const DnsServerList({
    required this.id,
    required this.country,
    required this.countryCode,
    required this.description,
    required this.servers,
  });
}

